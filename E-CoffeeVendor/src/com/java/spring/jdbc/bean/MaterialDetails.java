package com.java.spring.jdbc.bean;

public class MaterialDetails {
	
	private long coffee;
	private long sugar;
	private long creamer;
	public long getCoffee() {
		return coffee;
	}
	public void setCoffee(long coffee) {
		this.coffee = coffee;
	}
	public long getSugar() {
		return sugar;
	}
	public void setSugar(long sugar) {
		this.sugar = sugar;
	}
	public long getCreamer() {
		return creamer;
	}
	public void setCreamer(long creamer) {
		this.creamer = creamer;
	}
}
